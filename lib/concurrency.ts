// Shared by lib/github.ts (per-year contribution chunks) and
// lib/squad/valuation.ts (per-contributor profile fetches) — same
// bounded-worker-pool shape, pulled out once instead of copy-pasted twice.
export async function mapWithConcurrency<T, R>(
  items: T[],
  limit: number,
  fn: (item: T) => Promise<R>
): Promise<R[]> {
  const results: R[] = new Array(items.length);
  let next = 0;
  async function worker() {
    while (next < items.length) {
      const i = next++;
      results[i] = await fn(items[i]);
    }
  }
  await Promise.all(Array.from({ length: Math.min(limit, items.length) }, worker));
  return results;
}
